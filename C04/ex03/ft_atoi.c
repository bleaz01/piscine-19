int		ft_atoi(char *str)
{
	int i;
	int e;
	int result;

	i = 0;
	e = 1;
	result = 0;
	while (((str[i] == '\t') || (str[i] == '\n')) ||
			((str[i] == '\v') || (str[i] == '\f')) ||
		((str[i] == '\r') || (str[i] == ' ')) || (str[i] == '+'))
	{
		i++;
	}
	while ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			e = -e;
		i++;
	}
	while (((str[i] >= 48) && (str[i] <= 57)) && (str[i] != '\0'))
	{
		result = result * 10 + (str[i] - 48);
		i++;
	}
	return (e * result);
}
