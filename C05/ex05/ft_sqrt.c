int		ft_sqrt(int nb)
{
	int i;

	if (nb == 1)
		return (1);
	i = 0;
	while (i < (nb / 2) && (i < 46340))
	{
		if (i * i < nb)
			i++;
		if (i * i == nb)
			return (i);
		if (i * i > nb)
			return (0);
	}
	return (0);
}
